const fs = require('fs');
const path = require('path');

const ROOT_DIR = path.resolve(__dirname, '..');
const OUTPUT_FILE = path.join(ROOT_DIR, 'image-assets.ts');

const AVATAR_DIR = path.join(ROOT_DIR, '已有资料', '图片资源', '用户头像 - Avatar', '海外头像');
const BANNER_DIR = path.join(ROOT_DIR, '已有资料', '图片资源', '封面图 - Banner');
const AVATAR_LIMIT = 16;

function getMimeType(filename) {
  const ext = path.extname(filename).toLowerCase();
  if (ext === '.png') {
    return 'image/png';
  }
  if (ext === '.webp') {
    return 'image/webp';
  }
  return 'image/jpeg';
}

function encodeFile(filePath) {
  const buffer = fs.readFileSync(filePath);
  return {
    name: path.basename(filePath),
    mimeType: getMimeType(filePath),
    base64: buffer.toString('base64'),
  };
}

function readImageFiles(dirPath, limit) {
  const files = fs
    .readdirSync(dirPath)
    .filter((name) => /\.(png|jpe?g|webp)$/i.test(name))
    .sort((a, b) => a.localeCompare(b, 'en'))
    .slice(0, limit);

  return files.map((name) => encodeFile(path.join(dirPath, name)));
}

function createPlaceholderSvg(label, startColor, endColor) {
  return `<?xml version="1.0" encoding="UTF-8"?>
<svg width="1200" height="800" viewBox="0 0 1200 800" fill="none" xmlns="http://www.w3.org/2000/svg">
  <defs>
    <linearGradient id="bg" x1="0" y1="0" x2="1200" y2="800" gradientUnits="userSpaceOnUse">
      <stop offset="0" stop-color="${startColor}"/>
      <stop offset="1" stop-color="${endColor}"/>
    </linearGradient>
  </defs>
  <rect width="1200" height="800" rx="48" fill="url(#bg)"/>
  <circle cx="190" cy="168" r="68" fill="white" fill-opacity="0.18"/>
  <circle cx="1010" cy="618" r="98" fill="white" fill-opacity="0.12"/>
  <rect x="122" y="520" width="400" height="28" rx="14" fill="white" fill-opacity="0.28"/>
  <rect x="122" y="568" width="278" height="20" rx="10" fill="white" fill-opacity="0.18"/>
  <text x="122" y="402" fill="white" font-family="Arial, sans-serif" font-size="72" font-weight="700">${label}</text>
  <text x="122" y="454" fill="white" fill-opacity="0.78" font-family="Arial, sans-serif" font-size="28">Built-in placeholder asset for Data Fill</text>
</svg>`;
}

function encodeSvgAsset(name, svg) {
  return {
    name,
    mimeType: 'image/svg+xml',
    base64: Buffer.from(svg, 'utf8').toString('base64'),
  };
}

function buildModuleSource(assets) {
  return `// This file is auto-generated by scripts/generate-image-assets.js
// Do not edit manually.

export interface PackedImageAsset {
  name: string;
  mimeType: string;
  base64: string;
}

export interface PackedImageAssetMap {
  avatars: PackedImageAsset[];
  banners: PackedImageAsset[];
  placeholders: PackedImageAsset[];
}

export const PACKED_IMAGE_ASSETS: PackedImageAssetMap = ${JSON.stringify(assets, null, 2)};
`;
}

function main() {
  const avatars = readImageFiles(AVATAR_DIR, AVATAR_LIMIT);
  const banners = readImageFiles(BANNER_DIR);
  const placeholders = [
    encodeSvgAsset('placeholder-grid.svg', createPlaceholderSvg('Placeholder', '#0F172A', '#334155')),
    encodeSvgAsset('placeholder-lime.svg', createPlaceholderSvg('Demo Cover', '#0F766E', '#65A30D')),
    encodeSvgAsset('placeholder-sky.svg', createPlaceholderSvg('Empty State', '#1D4ED8', '#7C3AED')),
  ];

  const source = buildModuleSource({ avatars, banners, placeholders });
  fs.writeFileSync(OUTPUT_FILE, source, 'utf8');

  console.log(
    `Generated ${path.relative(ROOT_DIR, OUTPUT_FILE)} with ${avatars.length} avatars, ${banners.length} banners, ${placeholders.length} placeholders.`
  );
}

main();
